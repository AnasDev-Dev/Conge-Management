CREATE OR REPLACE FUNCTION credit_recuperation(p_user_id UUID, p_days FLOAT, p_date_from DATE, p_date_to DATE, p_reason TEXT)
RETURNS JSONB LANGUAGE plpgsql SECURITY DEFINER AS $$
DECLARE v_user utilisateurs%ROWTYPE; v_new_balance FLOAT;
BEGIN
  SELECT * INTO v_user FROM utilisateurs WHERE id = p_user_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'User % not found', p_user_id; END IF;
  IF p_days <= 0 THEN RAISE EXCEPTION 'Days must be positive'; END IF;
  v_new_balance := v_user.balance_recuperation + p_days;
  UPDATE utilisateurs SET balance_recuperation = v_new_balance WHERE id = p_user_id;
  INSERT INTO leave_balance_history (user_id, type, amount, reason, year, date_from, date_to)
  VALUES (p_user_id, 'RECUPERATION', p_days, COALESCE(p_reason, 'Credit recuperation'),
    EXTRACT(YEAR FROM p_date_from)::INT, p_date_from, p_date_to);
  RETURN jsonb_build_object('user_id', p_user_id, 'days_credited', p_days, 'new_balance', v_new_balance, 'reason', p_reason);
END;
$$;

CREATE OR REPLACE FUNCTION set_initial_balance(p_user_id UUID, p_balance FLOAT, p_year INTEGER, p_reason TEXT DEFAULT 'Initialisation solde par RH')
RETURNS JSONB LANGUAGE plpgsql SECURITY DEFINER AS $$
DECLARE v_user utilisateurs%ROWTYPE; v_old_balance FLOAT; v_max_balance FLOAT := 52.0; v_capped BOOLEAN := false; v_final_balance FLOAT;
BEGIN
  SELECT * INTO v_user FROM utilisateurs WHERE id = p_user_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'Utilisateur % introuvable', p_user_id; END IF;
  IF p_balance < 0 THEN RAISE EXCEPTION 'Le solde ne peut pas etre negatif'; END IF;
  v_old_balance := v_user.balance_conge;
  IF p_balance > v_max_balance THEN v_final_balance := v_max_balance; v_capped := true;
  ELSE v_final_balance := p_balance; END IF;
  UPDATE utilisateurs SET balance_conge = v_final_balance, updated_at = NOW() WHERE id = p_user_id;
  INSERT INTO leave_balance_history (user_id, type, amount, reason, year)
  VALUES (p_user_id, 'CONGE', v_final_balance,
    COALESCE(p_reason, 'Initialisation solde par RH') || ' (ancien solde: ' || v_old_balance || ')', p_year);
  RETURN jsonb_build_object('user_id', p_user_id, 'old_balance', v_old_balance, 'new_balance', v_final_balance,
    'year', p_year, 'capped', v_capped, 'max_balance', v_max_balance);
END;
$$;

CREATE OR REPLACE FUNCTION accrue_monthly_balance(p_year INT DEFAULT NULL, p_month INT DEFAULT NULL)
RETURNS JSONB LANGUAGE plpgsql SECURITY DEFINER AS $$
DECLARE v_user RECORD; v_entitlement JSONB; v_annual FLOAT; v_monthly FLOAT; v_cumulative FLOAT;
  v_year INT := COALESCE(p_year, EXTRACT(YEAR FROM CURRENT_DATE)::INT);
  v_month INT := COALESCE(p_month, EXTRACT(MONTH FROM CURRENT_DATE)::INT); v_count INT := 0;
BEGIN
  FOR v_user IN SELECT id FROM utilisateurs WHERE is_active = true LOOP
    v_entitlement := calculate_annual_entitlement(v_user.id);
    v_annual := (v_entitlement->>'annual_entitlement')::FLOAT;
    v_monthly := ROUND((v_annual / 12.0)::NUMERIC, 2)::FLOAT;
    SELECT COALESCE(cumulative_days, 0) INTO v_cumulative
    FROM monthly_balance_accrual WHERE user_id = v_user.id AND year = v_year AND month = v_month - 1;
    IF NOT FOUND THEN
      IF v_month = 1 THEN v_cumulative := 0; ELSE v_cumulative := v_monthly * (v_month - 1); END IF;
    END IF;
    v_cumulative := v_cumulative + v_monthly;
    INSERT INTO monthly_balance_accrual (user_id, year, month, accrued_days, cumulative_days, annual_entitlement)
    VALUES (v_user.id, v_year, v_month, v_monthly, v_cumulative, v_annual)
    ON CONFLICT (user_id, year, month) DO UPDATE SET accrued_days = EXCLUDED.accrued_days,
      cumulative_days = EXCLUDED.cumulative_days, annual_entitlement = EXCLUDED.annual_entitlement;
    v_count := v_count + 1;
  END LOOP;
  RETURN jsonb_build_object('employees_processed', v_count, 'year', v_year, 'month', v_month, 'status', 'completed');
END;
$$;

CREATE OR REPLACE FUNCTION submit_recovery_request(p_user_id UUID, p_days FLOAT, p_date_worked DATE, p_work_type TEXT, p_reason TEXT, p_period TEXT DEFAULT 'FULL')
RETURNS JSONB LANGUAGE plpgsql SECURITY DEFINER AS $$
DECLARE v_user utilisateurs%ROWTYPE; v_request recovery_requests%ROWTYPE; v_days FLOAT;
BEGIN
  SELECT * INTO v_user FROM utilisateurs WHERE id = p_user_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'Utilisateur introuvable'; END IF;
  IF p_period NOT IN ('MORNING', 'AFTERNOON', 'FULL') THEN RAISE EXCEPTION 'La periode doit etre MORNING, AFTERNOON ou FULL'; END IF;
  IF p_days IS NULL OR p_days = 0 THEN
    IF p_period = 'FULL' THEN v_days := 1.0; ELSE v_days := 0.5; END IF;
  ELSE v_days := p_days; END IF;
  IF v_days <= 0 OR v_days > 5 THEN RAISE EXCEPTION 'Le nombre de jours doit etre entre 0.5 et 5'; END IF;
  INSERT INTO recovery_requests (user_id, days, date_worked, work_type, period, reason)
  VALUES (p_user_id, v_days, p_date_worked, p_work_type::recovery_work_type, p_period, p_reason)
  RETURNING * INTO v_request;
  RETURN to_jsonb(v_request);
END;
$$;

CREATE OR REPLACE FUNCTION validate_recovery_request(p_request_id BIGINT, p_validator_id UUID)
RETURNS JSONB LANGUAGE plpgsql SECURITY DEFINER AS $$
DECLARE v_request recovery_requests%ROWTYPE; v_validator utilisateurs%ROWTYPE; v_new_balance FLOAT; v_expires_at DATE;
BEGIN
  SELECT * INTO v_request FROM recovery_requests WHERE id = p_request_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'Demande de recuperation introuvable'; END IF;
  IF v_request.status != 'PENDING' THEN RAISE EXCEPTION 'Cette demande a deja ete traitee'; END IF;
  SELECT * INTO v_validator FROM utilisateurs WHERE id = p_validator_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'Validateur introuvable'; END IF;
  IF v_validator.role::TEXT NOT IN ('CHEF_SERVICE', 'RH', 'DIRECTEUR_EXECUTIF', 'ADMIN') THEN
    RAISE EXCEPTION 'Seul un responsable peut valider les recuperations';
  END IF;
  UPDATE recovery_requests SET status = 'VALIDATED', validated_by = p_validator_id, validated_at = NOW(), updated_at = NOW() WHERE id = p_request_id;
  UPDATE utilisateurs SET balance_recuperation = balance_recuperation + v_request.days WHERE id = v_request.user_id RETURNING balance_recuperation INTO v_new_balance;
  v_expires_at := make_date(EXTRACT(YEAR FROM v_request.date_worked)::INT + 1, 6, 30);
  INSERT INTO recovery_balance_lots (user_id, days, remaining_days, year_acquired, expires_at, source_request_id)
  VALUES (v_request.user_id, v_request.days, v_request.days, EXTRACT(YEAR FROM v_request.date_worked)::INT, v_expires_at, p_request_id);
  INSERT INTO leave_balance_history (user_id, type, amount, reason, year, date_from, date_to, expires_at)
  VALUES (v_request.user_id, 'RECUPERATION', v_request.days,
    'Recuperation validee - ' || COALESCE(v_request.reason, v_request.work_type::TEXT),
    EXTRACT(YEAR FROM v_request.date_worked)::INT, v_request.date_worked, v_request.date_worked, v_expires_at);
  RETURN jsonb_build_object('request_id', p_request_id, 'status', 'VALIDATED', 'days', v_request.days, 'new_balance', v_new_balance, 'expires_at', v_expires_at);
END;
$$;

CREATE OR REPLACE FUNCTION reject_recovery_request(p_request_id BIGINT, p_rejector_id UUID, p_reason TEXT)
RETURNS JSONB LANGUAGE plpgsql SECURITY DEFINER AS $$
DECLARE v_request recovery_requests%ROWTYPE;
