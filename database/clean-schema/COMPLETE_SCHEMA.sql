CREATE OR REPLACE FUNCTION recalculate_all_balances()
RETURNS JSONB LANGUAGE plpgsql SECURITY DEFINER AS $$
DECLARE v_user RECORD; v_entitlement JSONB; v_annual FLOAT; v_used FLOAT; v_new_balance FLOAT;
  v_current_year INTEGER := EXTRACT(YEAR FROM CURRENT_DATE)::INTEGER; v_count INTEGER := 0;
BEGIN
  FOR v_user IN SELECT id, full_name FROM utilisateurs WHERE is_active = true LOOP
    v_entitlement := calculate_annual_entitlement(v_user.id);
    v_annual := (v_entitlement->>'annual_entitlement')::FLOAT;
    SELECT COALESCE(SUM(days_count), 0) INTO v_used FROM leave_requests
    WHERE user_id = v_user.id AND request_type = 'CONGE' AND status = 'APPROVED' AND EXTRACT(YEAR FROM start_date) = v_current_year;
    v_new_balance := GREATEST(v_annual - v_used, 0);
    UPDATE utilisateurs SET balance_conge = v_new_balance WHERE id = v_user.id;
    INSERT INTO leave_balance_history (user_id, type, amount, reason, year)
    VALUES (v_user.id, 'CONGE', v_new_balance, 'Recalcul automatique - Droit annuel ' || v_current_year, v_current_year);
    v_count := v_count + 1;
  END LOOP;
  RETURN jsonb_build_object('employees_updated', v_count, 'year', v_current_year, 'status', 'completed');
END;
$$;


