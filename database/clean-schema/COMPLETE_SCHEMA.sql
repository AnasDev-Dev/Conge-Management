CREATE OR REPLACE FUNCTION public.approve_mission_request(p_request_id BIGINT, p_approver_id UUID)
RETURNS JSONB LANGUAGE plpgsql SECURITY DEFINER SET search_path = public AS $$
DECLARE
  v_request mission_requests%ROWTYPE; v_approver utilisateurs%ROWTYPE;
  v_expected_role TEXT; v_next_status TEXT; v_field TEXT;
  v_requester_dept_id BIGINT; v_creator_role TEXT;
BEGIN
  SELECT * INTO v_request FROM mission_requests WHERE id = p_request_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'Demande de mission #% introuvable', p_request_id; END IF;
  SELECT * INTO v_approver FROM utilisateurs WHERE id = p_approver_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'Approbateur introuvable'; END IF;
  IF p_approver_id = v_request.user_id THEN RAISE EXCEPTION 'Vous ne pouvez pas approuver votre propre demande'; END IF;

  CASE v_request.status
    WHEN 'PENDING'::public.leave_status THEN v_expected_role := 'CHEF_SERVICE'; v_next_status := 'VALIDATED_DC'; v_field := 'dc';
    WHEN 'VALIDATED_DC'::public.leave_status THEN v_expected_role := 'RH'; v_next_status := 'VALIDATED_RP'; v_field := 'rp';
    WHEN 'VALIDATED_RP'::public.leave_status THEN v_expected_role := 'DIRECTEUR_EXECUTIF'; v_next_status := 'APPROVED'; v_field := 'de';
    ELSE RAISE EXCEPTION 'La demande de mission #% est au statut %, elle ne peut pas etre approuvee', p_request_id, v_request.status;
  END CASE;

  IF v_approver.role::TEXT != v_expected_role AND v_approver.role::TEXT != 'ADMIN' THEN
    RAISE EXCEPTION 'Seul le role % peut approuver a cette etape (vous etes %)', v_expected_role, v_approver.role;
  END IF;

  IF v_approver.role::TEXT = 'CHEF_SERVICE' THEN
    SELECT department_id INTO v_requester_dept_id FROM utilisateurs WHERE id = v_request.user_id;
    IF v_approver.department_id IS NULL OR v_requester_dept_id IS NULL OR v_approver.department_id != v_requester_dept_id THEN
      RAISE EXCEPTION 'Le chef de service ne peut approuver que les demandes de son departement';
    END IF;
  END IF;

  EXECUTE format('UPDATE mission_requests SET status = $1::public.leave_status, approved_by_%s = $2, approved_at_%s = NOW(), updated_at = NOW() WHERE id = $3', v_field, v_field)
  USING v_next_status, p_approver_id, p_request_id;

  IF v_next_status = 'APPROVED' THEN
    UPDATE mission_requests SET director_decision = 'ACCORDEE' WHERE id = p_request_id;
  END IF;

  IF v_next_status = 'VALIDATED_DC' AND v_request.created_by IS NOT NULL THEN
    SELECT role::TEXT INTO v_creator_role FROM utilisateurs WHERE id = v_request.created_by;
    IF v_creator_role = 'RH' THEN
      UPDATE mission_requests SET status = 'VALIDATED_RP'::public.leave_status,
        approved_by_rp = v_request.created_by, approved_at_rp = NOW(), updated_at = NOW()
      WHERE id = p_request_id;
    END IF;
  END IF;

  RETURN (SELECT to_jsonb(mr.*) FROM mission_requests mr WHERE mr.id = p_request_id);
END;
$$;

CREATE OR REPLACE FUNCTION public.reject_mission_request(p_request_id BIGINT, p_rejector_id UUID, p_reason TEXT)
RETURNS JSONB LANGUAGE plpgsql SECURITY DEFINER SET search_path = public AS $$
DECLARE
  v_request mission_requests%ROWTYPE; v_rejector utilisateurs%ROWTYPE; v_requester_dept_id BIGINT;
BEGIN
  SELECT * INTO v_request FROM mission_requests WHERE id = p_request_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'Demande de mission #% introuvable', p_request_id; END IF;
  SELECT * INTO v_rejector FROM utilisateurs WHERE id = p_rejector_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'Utilisateur introuvable'; END IF;
  IF p_reason IS NULL OR TRIM(p_reason) = '' THEN RAISE EXCEPTION 'La raison du rejet est obligatoire'; END IF;

  IF v_rejector.role::TEXT != 'ADMIN' THEN
    CASE v_request.status
      WHEN 'PENDING'::public.leave_status THEN
        IF v_rejector.role::TEXT != 'CHEF_SERVICE' THEN RAISE EXCEPTION 'Seul le Chef de Service peut rejeter a cette etape'; END IF;
        SELECT department_id INTO v_requester_dept_id FROM utilisateurs WHERE id = v_request.user_id;
        IF v_rejector.department_id IS NULL OR v_requester_dept_id IS NULL OR v_rejector.department_id != v_requester_dept_id THEN
          RAISE EXCEPTION 'Le chef de service ne peut rejeter que les demandes de son departement';
        END IF;
      WHEN 'VALIDATED_DC'::public.leave_status THEN
        IF v_rejector.role::TEXT != 'RH' THEN RAISE EXCEPTION 'Seul le RH peut rejeter a cette etape'; END IF;
      WHEN 'VALIDATED_RP'::public.leave_status THEN
        IF v_rejector.role::TEXT != 'DIRECTEUR_EXECUTIF' THEN RAISE EXCEPTION 'Seul le Directeur Executif peut rejeter a cette etape'; END IF;
      ELSE RAISE EXCEPTION 'La demande de mission #% est au statut %, elle ne peut pas etre rejetee', p_request_id, v_request.status;
    END CASE;
  ELSE
    IF v_request.status NOT IN ('PENDING'::public.leave_status, 'VALIDATED_DC'::public.leave_status, 'VALIDATED_RP'::public.leave_status) THEN
      RAISE EXCEPTION 'La demande de mission #% est au statut %, elle ne peut pas etre rejetee', p_request_id, v_request.status;
    END IF;
  END IF;

  UPDATE mission_requests SET status = 'REJECTED'::public.leave_status, rejected_by = p_rejector_id, rejected_at = NOW(),
    rejection_reason = TRIM(p_reason),
    director_decision = CASE WHEN v_rejector.role::TEXT IN ('DIRECTEUR_EXECUTIF', 'ADMIN') THEN 'REFUSEE' ELSE director_decision END,
    updated_at = NOW() WHERE id = p_request_id;
  RETURN (SELECT to_jsonb(mr.*) FROM mission_requests mr WHERE mr.id = p_request_id);
END;
$$;

CREATE OR REPLACE FUNCTION public.undo_approve_mission_request(p_request_id BIGINT, p_user_id UUID)
RETURNS JSONB LANGUAGE plpgsql SECURITY DEFINER SET search_path = public AS $$
DECLARE
  v_request mission_requests%ROWTYPE; v_user utilisateurs%ROWTYPE; v_prev_status TEXT; v_field TEXT;
BEGIN
  SELECT * INTO v_request FROM mission_requests WHERE id = p_request_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'Demande de mission #% introuvable', p_request_id; END IF;
  SELECT * INTO v_user FROM utilisateurs WHERE id = p_user_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'Utilisateur introuvable'; END IF;
  IF v_request.status = v_request.initial_status THEN RAISE EXCEPTION 'Impossible d''annuler: la demande est a son statut initial'; END IF;

  CASE v_request.status
    WHEN 'VALIDATED_DC'::public.leave_status THEN
      v_prev_status := 'PENDING'; v_field := 'dc';
      IF v_user.role::TEXT != 'ADMIN' AND v_request.approved_by_dc != p_user_id THEN RAISE EXCEPTION 'Seul l''approbateur ou un admin peut annuler cette validation'; END IF;
      IF v_request.initial_status = 'VALIDATED_DC'::public.leave_status THEN RAISE EXCEPTION 'Impossible d''annuler: la demande est a son statut initial'; END IF;
    WHEN 'VALIDATED_RP'::public.leave_status THEN
      v_prev_status := 'VALIDATED_DC'; v_field := 'rp';
      IF v_user.role::TEXT != 'ADMIN' AND v_request.approved_by_rp != p_user_id THEN RAISE EXCEPTION 'Seul l''approbateur ou un admin peut annuler cette validation'; END IF;
      IF v_request.initial_status = 'VALIDATED_RP'::public.leave_status THEN RAISE EXCEPTION 'Impossible d''annuler: la demande est a son statut initial'; END IF;
    WHEN 'APPROVED'::public.leave_status THEN
      v_prev_status := 'VALIDATED_RP'; v_field := 'de';
      IF v_user.role::TEXT != 'ADMIN' AND v_request.approved_by_de != p_user_id THEN RAISE EXCEPTION 'Seul l''approbateur ou un admin peut annuler cette validation'; END IF;
      UPDATE mission_requests SET director_decision = NULL WHERE id = p_request_id;
    ELSE RAISE EXCEPTION 'La demande de mission #% est au statut %, impossible d''annuler', p_request_id, v_request.status;
  END CASE;

  EXECUTE format('UPDATE mission_requests SET status = $1::public.leave_status, approved_by_%s = NULL, approved_at_%s = NULL, updated_at = NOW() WHERE id = $2', v_field, v_field)
  USING v_prev_status, p_request_id;
  RETURN (SELECT to_jsonb(mr.*) FROM mission_requests mr WHERE mr.id = p_request_id);
END;
$$;

