    WHEN NEW.status = 'PENDING'::leave_status AND OLD.status = 'REJECTED'::leave_status THEN
      v_title := 'Demande restauree';
      v_message := format('Votre demande de %s du %s a ete restauree et est de nouveau en attente.', v_type_label, v_dates_text);
