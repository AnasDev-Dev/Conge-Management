-- ── Leave: Compute initial status based on creator's role (company-aware) ──

DROP TRIGGER IF EXISTS trg_compute_initial_leave_status ON public.leave_requests;
DROP FUNCTION IF EXISTS public.compute_initial_leave_status() CASCADE;

CREATE OR REPLACE FUNCTION public.compute_initial_leave_status()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_creator_role TEXT;
BEGIN
  IF auth.uid() IS NOT NULL THEN
    NEW.created_by := auth.uid();
  END IF;

  IF auth.uid() IS NULL THEN
    NEW.initial_status := COALESCE(NEW.status, 'PENDING'::public.leave_status);
    NEW.status := COALESCE(NEW.status, 'PENDING'::public.leave_status);
    RETURN NEW;
  END IF;

  v_creator_role := get_my_role();

  IF v_creator_role IN ('DIRECTEUR_EXECUTIF', 'ADMIN') THEN
    NEW.status := 'APPROVED'::public.leave_status;
    NEW.approved_by_de := auth.uid();
    NEW.approved_at_de := NOW();
    NEW.initial_status := 'APPROVED'::public.leave_status;
    RETURN NEW;
  END IF;

  IF v_creator_role = 'RH' AND NEW.user_id = auth.uid() THEN
    NEW.status := 'VALIDATED_DC'::public.leave_status;
    NEW.approved_by_rp := auth.uid();
    NEW.approved_at_rp := NOW();
    NEW.initial_status := 'VALIDATED_DC'::public.leave_status;
    RETURN NEW;
  END IF;

  IF v_creator_role = 'RH' AND NEW.user_id != auth.uid() THEN
    NEW.status := 'VALIDATED_RP'::public.leave_status;
    NEW.approved_by_rp := auth.uid();
    NEW.approved_at_rp := NOW();
    NEW.initial_status := 'VALIDATED_RP'::public.leave_status;
    RETURN NEW;
  END IF;

  IF v_creator_role = 'CHEF_SERVICE' THEN
    NEW.status := 'VALIDATED_DC'::public.leave_status;
    NEW.approved_by_dc := auth.uid();
    NEW.approved_at_dc := NOW();
    NEW.initial_status := 'VALIDATED_DC'::public.leave_status;
    RETURN NEW;
  END IF;

  NEW.status := 'PENDING'::public.leave_status;
  NEW.initial_status := 'PENDING'::public.leave_status;
  RETURN NEW;
END;
$$;

CREATE TRIGGER trg_compute_initial_leave_status
  BEFORE INSERT ON public.leave_requests
  FOR EACH ROW
  EXECUTE FUNCTION public.compute_initial_leave_status();


-- ── Leave: Handle balance for auto-approved requests (RECUPERATION only + FIFO) ──

DROP TRIGGER IF EXISTS trg_handle_auto_approved_leave ON public.leave_requests;
DROP FUNCTION IF EXISTS public.handle_auto_approved_leave() CASCADE;

CREATE OR REPLACE FUNCTION public.handle_auto_approved_leave()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  IF NEW.status = 'APPROVED'::public.leave_status THEN
    -- CONGE: NO balance deduction (tracked dynamically via calculate_leave_balance)
    -- RECUPERATION: deduct from utilisateurs + recovery_balance_lots FIFO
    IF COALESCE(NEW.balance_recuperation_used, 0) > 0 THEN
      UPDATE utilisateurs
      SET balance_recuperation = GREATEST(balance_recuperation - NEW.balance_recuperation_used, 0)
      WHERE id = NEW.user_id;

      DECLARE
        v_lot RECORD;
        v_remaining FLOAT := NEW.balance_recuperation_used;
      BEGIN
        FOR v_lot IN
          SELECT id, remaining_days FROM recovery_balance_lots
          WHERE user_id = NEW.user_id AND expired = false AND remaining_days > 0
          ORDER BY expires_at ASC, id ASC
        LOOP
          EXIT WHEN v_remaining <= 0;
          IF v_lot.remaining_days >= v_remaining THEN
            UPDATE recovery_balance_lots SET remaining_days = remaining_days - v_remaining WHERE id = v_lot.id;
            v_remaining := 0;
          ELSE
            v_remaining := v_remaining - v_lot.remaining_days;
            UPDATE recovery_balance_lots SET remaining_days = 0 WHERE id = v_lot.id;
          END IF;
        END LOOP;
      END;
    END IF;

    -- Audit trail (no balance mutation for CONGE)
    IF COALESCE(NEW.balance_conge_used, 0) > 0 THEN
      INSERT INTO leave_balance_history (user_id, type, amount, reason, year, date_from, date_to)
      VALUES (NEW.user_id, 'CONGE', -NEW.balance_conge_used,
        'Demande auto-approuvee #' || NEW.id || ' (conge)',
        EXTRACT(YEAR FROM NEW.start_date)::INT, NEW.start_date, NEW.end_date);
    END IF;

    IF COALESCE(NEW.balance_recuperation_used, 0) > 0 THEN
      INSERT INTO leave_balance_history (user_id, type, amount, reason, year, date_from, date_to)
      VALUES (NEW.user_id, 'RECUPERATION', -NEW.balance_recuperation_used,
        'Demande auto-approuvee #' || NEW.id || ' (recuperation)',
        EXTRACT(YEAR FROM NEW.start_date)::INT, NEW.start_date, NEW.end_date);
    END IF;
  END IF;

  RETURN NEW;
END;
$$;

CREATE TRIGGER trg_handle_auto_approved_leave
  AFTER INSERT ON public.leave_requests
  FOR EACH ROW
  EXECUTE FUNCTION public.handle_auto_approved_leave();


