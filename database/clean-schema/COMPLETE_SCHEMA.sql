-- ── count_working_days (7-arg ONLY — from 08_working_days_per_department) ──
-- Lookup: department → category → company default → hardcoded Moroccan standard

DROP FUNCTION IF EXISTS count_working_days(DATE, DATE, BIGINT) CASCADE;
DROP FUNCTION IF EXISTS count_working_days(DATE, DATE, BIGINT, BIGINT, TEXT, TEXT) CASCADE;

CREATE OR REPLACE FUNCTION count_working_days(
  p_start_date     DATE,
  p_end_date       DATE,
  p_company_id     BIGINT DEFAULT NULL,
  p_category_id    BIGINT DEFAULT NULL,
  p_start_half_day TEXT DEFAULT 'FULL',
  p_end_half_day   TEXT DEFAULT 'FULL',
  p_department_id  BIGINT DEFAULT NULL
)
RETURNS FLOAT
LANGUAGE plpgsql
STABLE
AS $$
DECLARE
  v_count      FLOAT := 0;
  v_current    DATE := p_start_date;
  v_dow        INTEGER;
  v_company    BIGINT;
  v_is_holiday BOOLEAN;
  v_wd         RECORD;
  v_day_active BOOLEAN;
  v_morning    BOOLEAN;
  v_afternoon  BOOLEAN;
  v_day_value  FLOAT;
BEGIN
  v_company := COALESCE(p_company_id, (SELECT id FROM public.companies LIMIT 1));

  -- Priority 1: Department-specific
  IF p_department_id IS NOT NULL THEN
    SELECT * INTO v_wd FROM public.working_days
    WHERE company_id = v_company AND department_id = p_department_id LIMIT 1;
  END IF;

  -- Priority 2: Category-specific
  IF NOT FOUND AND p_category_id IS NOT NULL THEN
    SELECT * INTO v_wd FROM public.working_days
    WHERE category_id = p_category_id LIMIT 1;
  END IF;

  -- Priority 3: Company default
  IF NOT FOUND OR (p_department_id IS NULL AND p_category_id IS NULL) THEN
    SELECT * INTO v_wd FROM public.working_days
    WHERE company_id = v_company AND department_id IS NULL AND category_id IS NULL LIMIT 1;
  END IF;

  -- Priority 4: Hardcoded Moroccan standard
  IF NOT FOUND THEN
    v_wd.monday := true; v_wd.tuesday := true; v_wd.wednesday := true;
    v_wd.thursday := true; v_wd.friday := true; v_wd.saturday := true; v_wd.sunday := false;
    v_wd.monday_morning := true; v_wd.monday_afternoon := true;
    v_wd.tuesday_morning := true; v_wd.tuesday_afternoon := true;
    v_wd.wednesday_morning := true; v_wd.wednesday_afternoon := true;
    v_wd.thursday_morning := true; v_wd.thursday_afternoon := true;
    v_wd.friday_morning := true; v_wd.friday_afternoon := true;
    v_wd.saturday_morning := true; v_wd.saturday_afternoon := false;
    v_wd.sunday_morning := false; v_wd.sunday_afternoon := false;
  END IF;

  WHILE v_current <= p_end_date LOOP
    v_dow := EXTRACT(ISODOW FROM v_current)::INTEGER;

    v_day_active := CASE v_dow
      WHEN 1 THEN v_wd.monday WHEN 2 THEN v_wd.tuesday WHEN 3 THEN v_wd.wednesday
      WHEN 4 THEN v_wd.thursday WHEN 5 THEN v_wd.friday WHEN 6 THEN v_wd.saturday
      WHEN 7 THEN v_wd.sunday ELSE false
    END;

    IF v_day_active THEN
      SELECT EXISTS(
        SELECT 1 FROM public.holidays h
        WHERE h.company_id = v_company
        AND (
          (h.is_recurring = true
           AND EXTRACT(MONTH FROM h.date) = EXTRACT(MONTH FROM v_current)
           AND EXTRACT(DAY FROM h.date) = EXTRACT(DAY FROM v_current))
          OR (h.is_recurring = false AND h.date = v_current)
        )
      ) INTO v_is_holiday;

      IF NOT v_is_holiday THEN
        v_morning := COALESCE(CASE v_dow
          WHEN 1 THEN v_wd.monday_morning WHEN 2 THEN v_wd.tuesday_morning
          WHEN 3 THEN v_wd.wednesday_morning WHEN 4 THEN v_wd.thursday_morning
          WHEN 5 THEN v_wd.friday_morning WHEN 6 THEN v_wd.saturday_morning
          WHEN 7 THEN v_wd.sunday_morning END, true);

        v_afternoon := COALESCE(CASE v_dow
          WHEN 1 THEN v_wd.monday_afternoon WHEN 2 THEN v_wd.tuesday_afternoon
          WHEN 3 THEN v_wd.wednesday_afternoon WHEN 4 THEN v_wd.thursday_afternoon
          WHEN 5 THEN v_wd.friday_afternoon WHEN 6 THEN v_wd.saturday_afternoon
          WHEN 7 THEN v_wd.sunday_afternoon END, false);

        v_day_value := 0;
        IF v_morning THEN v_day_value := v_day_value + 0.5; END IF;
        IF v_afternoon THEN v_day_value := v_day_value + 0.5; END IF;

        IF v_current = p_start_date AND p_start_half_day = 'AFTERNOON' AND v_morning THEN
          v_day_value := v_day_value - 0.5;
        END IF;
        IF v_current = p_end_date AND p_end_half_day = 'MORNING' AND v_afternoon THEN
          v_day_value := v_day_value - 0.5;
        END IF;

        v_count := v_count + GREATEST(v_day_value, 0);
      END IF;
    END IF;

    v_current := v_current + 1;
  END LOOP;

  RETURN v_count;
END;
$$;


-- ── calculate_annual_entitlement (department-based — Balance Model V2) ──

CREATE OR REPLACE FUNCTION calculate_annual_entitlement(p_user_id UUID)
RETURNS JSONB
LANGUAGE plpgsql
STABLE
AS $$
DECLARE
  v_user              utilisateurs%ROWTYPE;
  v_dept_days         FLOAT;
  v_seniority_date    DATE;
  v_years_of_service  FLOAT;
  v_seniority_periods INTEGER;
  v_base_days         FLOAT := 18.0;
  v_bonus_days        FLOAT := 0;
  v_total_entitlement FLOAT;
  v_max_days          FLOAT := 30.0;
  v_is_minor          BOOLEAN := false;
  v_source            TEXT := 'default';
BEGIN
  SELECT * INTO v_user FROM utilisateurs WHERE id = p_user_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'User % not found', p_user_id; END IF;

  -- Simple priority: employee override > department default > 18
  IF v_user.annual_leave_days IS NOT NULL THEN
    v_base_days := v_user.annual_leave_days;
    v_source := 'employee';
  ELSIF v_user.department_id IS NOT NULL THEN
    SELECT annual_leave_days INTO v_dept_days
    FROM departments WHERE id = v_user.department_id;
    IF FOUND AND v_dept_days IS NOT NULL THEN
      v_base_days := v_dept_days;
      v_source := 'department';
    END IF;
  END IF;

  -- Use date_anciennete for seniority calc, fallback to hire_date
  v_seniority_date := COALESCE(v_user.date_anciennete, v_user.hire_date, CURRENT_DATE);

  IF v_user.birth_date IS NOT NULL THEN
    v_is_minor := (age(CURRENT_DATE, v_user.birth_date) < interval '18 years');
  END IF;
  IF v_is_minor THEN v_base_days := GREATEST(v_base_days, 24.0); END IF;

  v_years_of_service := EXTRACT(EPOCH FROM age(CURRENT_DATE, v_seniority_date)) / (365.25 * 86400);
  v_seniority_periods := FLOOR(v_years_of_service / 5.0)::INTEGER;
  v_bonus_days := v_seniority_periods * 1.5;
  v_total_entitlement := LEAST(v_base_days + v_bonus_days, v_max_days);

  RETURN jsonb_build_object(
    'user_id', p_user_id,
    'seniority_date', v_seniority_date,
    'hire_date', v_user.hire_date,
    'date_anciennete', v_user.date_anciennete,
    'annual_leave_days', v_user.annual_leave_days,
    'years_of_service', ROUND(v_years_of_service::NUMERIC, 1),
    'seniority_periods', v_seniority_periods,
    'base_days', v_base_days,
    'bonus_days', v_bonus_days,
    'annual_entitlement', v_total_entitlement,
    'is_minor', v_is_minor,
    'max_days', v_max_days,
    'department_id', v_user.department_id,
    'entitlement_source', v_source
  );
END;
$$;


-- ── calculate_leave_balance (carry-over + dept entitlement — Balance Model V2) ──

CREATE OR REPLACE FUNCTION calculate_leave_balance(p_user_id UUID)
RETURNS JSONB
LANGUAGE plpgsql
STABLE
AS $$
DECLARE
  v_user                utilisateurs%ROWTYPE;
  v_entitlement         JSONB;
  v_annual_entitlement  FLOAT;
  v_current_year        INTEGER := EXTRACT(YEAR FROM CURRENT_DATE)::INTEGER;
  v_current_month       INTEGER := EXTRACT(MONTH FROM CURRENT_DATE)::INTEGER;
  v_days_used_this_year FLOAT := 0;
  v_days_pending        FLOAT := 0;
  v_recup_used          FLOAT := 0;
  v_recup_pending       FLOAT := 0;
  v_monthly_accrued     FLOAT := 0;
  v_max_balance         FLOAT := 52.0;
  v_is_max_reached      BOOLEAN := false;
  v_recup_expires_at    DATE;
BEGIN
  SELECT * INTO v_user FROM utilisateurs WHERE id = p_user_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'User % not found', p_user_id; END IF;

  v_entitlement := calculate_annual_entitlement(p_user_id);
  v_annual_entitlement := (v_entitlement->>'annual_entitlement')::FLOAT;

  SELECT COALESCE(SUM(days_count), 0) INTO v_days_used_this_year
  FROM leave_requests WHERE user_id = p_user_id AND request_type = 'CONGE'
    AND status = 'APPROVED' AND EXTRACT(YEAR FROM start_date) = v_current_year;

  SELECT COALESCE(SUM(days_count), 0) INTO v_days_pending
  FROM leave_requests WHERE user_id = p_user_id AND request_type = 'CONGE'
    AND status IN ('PENDING', 'VALIDATED_RP', 'VALIDATED_DC')
    AND EXTRACT(YEAR FROM start_date) = v_current_year;

  SELECT COALESCE(SUM(days_count), 0) INTO v_recup_used
  FROM leave_requests WHERE user_id = p_user_id AND request_type = 'RECUPERATION'
    AND status = 'APPROVED' AND EXTRACT(YEAR FROM start_date) = v_current_year;

  SELECT COALESCE(SUM(days_count), 0) INTO v_recup_pending
  FROM leave_requests WHERE user_id = p_user_id AND request_type = 'RECUPERATION'
    AND status IN ('PENDING', 'VALIDATED_RP', 'VALIDATED_DC')
    AND EXTRACT(YEAR FROM start_date) = v_current_year;

  -- Monthly accrual from department entitlement (not balance_conge)
  v_monthly_accrued := ROUND((v_annual_entitlement / 12.0 * v_current_month)::NUMERIC, 2)::FLOAT;
  v_is_max_reached := v_user.balance_conge >= v_max_balance;
  v_recup_expires_at := make_date(v_current_year + 1, 6, 30);

  RETURN jsonb_build_object(
    'user_id', p_user_id,
    'carry_over', v_user.balance_conge,
    'balance_conge', v_user.balance_conge,
    'balance_recuperation', v_user.balance_recuperation,
    'annual_entitlement', v_annual_entitlement,
    'days_used_this_year', v_days_used_this_year,
    'days_pending', v_days_pending,
    'recup_used', v_recup_used,
    'recup_pending', v_recup_pending,
    'entitlement_details', v_entitlement,
    'monthly_accrued', v_monthly_accrued,
    'monthly_rate', ROUND((v_annual_entitlement / 12.0)::NUMERIC, 2)::FLOAT,
    'available_now', GREATEST(v_user.balance_conge + v_monthly_accrued - v_days_used_this_year - v_days_pending, 0),
    'is_max_reached', v_is_max_reached,
    'max_balance', v_max_balance,
    'recup_expires_at', v_recup_expires_at
  );
END;
$$;


-- ── approve_leave_request (FINAL — from 10_combined: FIFO lots, no CONGE deduction, dept-aware) ──

DROP FUNCTION IF EXISTS public.approve_leave_request(BIGINT, UUID, DATE, DATE, FLOAT) CASCADE;

CREATE OR REPLACE FUNCTION public.approve_leave_request(
  p_request_id     BIGINT,
  p_approver_id    UUID,
  p_new_start_date DATE DEFAULT NULL,
  p_new_end_date   DATE DEFAULT NULL,
  p_new_days_count FLOAT DEFAULT NULL
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_request            leave_requests%ROWTYPE;
  v_approver           utilisateurs%ROWTYPE;
  v_request_user       utilisateurs%ROWTYPE;
  v_expected_role      TEXT;
  v_next_status        TEXT;
  v_field              TEXT;
  v_days               FLOAT;
  v_balance_field      TEXT;
  v_requester_dept_id  BIGINT;
  v_company_id         BIGINT;
  v_conge_used         FLOAT;
  v_recup_used         FLOAT;
  v_lot                RECORD;
  v_remaining_to_deduct FLOAT;
BEGIN
  SELECT * INTO v_request FROM leave_requests WHERE id = p_request_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'Demande #% introuvable', p_request_id; END IF;

  SELECT * INTO v_approver FROM utilisateurs WHERE id = p_approver_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'Approbateur introuvable'; END IF;

  IF p_approver_id = v_request.user_id THEN
    RAISE EXCEPTION 'Vous ne pouvez pas approuver votre propre demande';
  END IF;

  CASE v_request.status
    WHEN 'PENDING'::public.leave_status THEN
      v_expected_role := 'RH'; v_next_status := 'VALIDATED_RP'; v_field := 'rp';
    WHEN 'VALIDATED_RP'::public.leave_status THEN
      v_expected_role := 'CHEF_SERVICE'; v_next_status := 'VALIDATED_DC'; v_field := 'dc';
    WHEN 'VALIDATED_DC'::public.leave_status THEN
      v_expected_role := 'DIRECTEUR_EXECUTIF'; v_next_status := 'APPROVED'; v_field := 'de';
    ELSE
      RAISE EXCEPTION 'La demande #% est au statut %, elle ne peut pas etre approuvee',
        p_request_id, v_request.status;
  END CASE;

  IF v_approver.role::TEXT != v_expected_role AND v_approver.role::TEXT != 'ADMIN' THEN
    RAISE EXCEPTION 'Seul le role % peut approuver a cette etape (vous etes %)',
      v_expected_role, v_approver.role;
  END IF;

  IF v_approver.role::TEXT = 'CHEF_SERVICE' THEN
    SELECT department_id INTO v_requester_dept_id FROM utilisateurs WHERE id = v_request.user_id;
    IF v_approver.department_id IS NULL OR v_requester_dept_id IS NULL
       OR v_approver.department_id != v_requester_dept_id THEN
      RAISE EXCEPTION 'Le chef de service ne peut approuver que les demandes de son departement';
    END IF;
  END IF;

  IF v_field = 'rp' AND p_new_start_date IS NOT NULL AND p_new_end_date IS NOT NULL THEN
    UPDATE leave_requests SET start_date = p_new_start_date, end_date = p_new_end_date,
      days_count = COALESCE(p_new_days_count, v_request.days_count) WHERE id = p_request_id;
    SELECT * INTO v_request FROM leave_requests WHERE id = p_request_id;
  END IF;

  v_days := v_request.days_count;

  EXECUTE format(
    'UPDATE leave_requests SET status = $1::public.leave_status, approved_by_%s = $2, approved_at_%s = NOW(), updated_at = NOW() WHERE id = $3',
    v_field, v_field
  ) USING v_next_status, p_approver_id, p_request_id;

  IF v_next_status = 'APPROVED' THEN
    SELECT * INTO v_request_user FROM utilisateurs WHERE id = v_request.user_id;
    v_company_id := COALESCE(v_request_user.company_id, (SELECT id FROM companies LIMIT 1));

    v_days := count_working_days(
      v_request.start_date, v_request.end_date,
      v_company_id, NULL, 'FULL', 'FULL', v_request_user.department_id
    );
    UPDATE leave_requests SET days_count = v_days WHERE id = p_request_id;

    v_conge_used := COALESCE(v_request.balance_conge_used, 0);
    v_recup_used := COALESCE(v_request.balance_recuperation_used, 0);

    IF v_conge_used = 0 AND v_recup_used = 0 THEN
      IF v_request.request_type = 'CONGE' THEN v_conge_used := v_days;
      ELSE v_recup_used := v_days; END IF;
    END IF;

    -- CONGE: audit trail only
    IF v_conge_used > 0 THEN
      INSERT INTO leave_balance_history (user_id, type, amount, reason, year, date_from, date_to)
      VALUES (v_request.user_id, 'CONGE', -v_conge_used,
        'Approbation demande #' || p_request_id || ' (conge)',
        EXTRACT(YEAR FROM v_request.start_date)::INT, v_request.start_date, v_request.end_date);
    END IF;

    -- RECUPERATION: deduct + FIFO lots
    IF v_recup_used > 0 THEN
      UPDATE utilisateurs SET balance_recuperation = GREATEST(balance_recuperation - v_recup_used, 0)
      WHERE id = v_request.user_id;

      INSERT INTO leave_balance_history (user_id, type, amount, reason, year, date_from, date_to)
      VALUES (v_request.user_id, 'RECUPERATION', -v_recup_used,
        'Approbation demande #' || p_request_id || ' (recuperation)',
        EXTRACT(YEAR FROM v_request.start_date)::INT, v_request.start_date, v_request.end_date);

      v_remaining_to_deduct := v_recup_used;
      FOR v_lot IN
        SELECT id, remaining_days FROM recovery_balance_lots
        WHERE user_id = v_request.user_id AND expired = false AND remaining_days > 0
        ORDER BY expires_at ASC, id ASC
      LOOP
        EXIT WHEN v_remaining_to_deduct <= 0;
        IF v_lot.remaining_days >= v_remaining_to_deduct THEN
          UPDATE recovery_balance_lots SET remaining_days = remaining_days - v_remaining_to_deduct WHERE id = v_lot.id;
          v_remaining_to_deduct := 0;
        ELSE
          v_remaining_to_deduct := v_remaining_to_deduct - v_lot.remaining_days;
          UPDATE recovery_balance_lots SET remaining_days = 0 WHERE id = v_lot.id;
        END IF;
      END LOOP;
    END IF;
  END IF;

  RETURN (SELECT to_jsonb(lr.*) FROM leave_requests lr WHERE lr.id = p_request_id);
END;
$$;


-- ── reject_leave_request ──

DROP FUNCTION IF EXISTS public.reject_leave_request(BIGINT, UUID, TEXT) CASCADE;

CREATE OR REPLACE FUNCTION public.reject_leave_request(
  p_request_id BIGINT, p_rejector_id UUID, p_reason TEXT
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_request leave_requests%ROWTYPE;
  v_rejector utilisateurs%ROWTYPE;
  v_requester_dept_id BIGINT;
BEGIN
  SELECT * INTO v_request FROM leave_requests WHERE id = p_request_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'Demande #% introuvable', p_request_id; END IF;

  SELECT * INTO v_rejector FROM utilisateurs WHERE id = p_rejector_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'Utilisateur introuvable'; END IF;

  IF p_reason IS NULL OR TRIM(p_reason) = '' THEN
    RAISE EXCEPTION 'La raison du rejet est obligatoire';
  END IF;

  IF v_rejector.role::TEXT != 'ADMIN' THEN
    CASE v_request.status
      WHEN 'PENDING'::public.leave_status THEN
        IF v_rejector.role::TEXT != 'RH' THEN RAISE EXCEPTION 'Seul le RH peut rejeter a cette etape'; END IF;
      WHEN 'VALIDATED_RP'::public.leave_status THEN
        IF v_rejector.role::TEXT != 'CHEF_SERVICE' THEN RAISE EXCEPTION 'Seul le Chef de Service peut rejeter a cette etape'; END IF;
        SELECT department_id INTO v_requester_dept_id FROM utilisateurs WHERE id = v_request.user_id;
        IF v_rejector.department_id IS NULL OR v_requester_dept_id IS NULL OR v_rejector.department_id != v_requester_dept_id THEN
          RAISE EXCEPTION 'Le chef de service ne peut rejeter que les demandes de son departement';
        END IF;
      WHEN 'VALIDATED_DC'::public.leave_status THEN
        IF v_rejector.role::TEXT != 'DIRECTEUR_EXECUTIF' THEN RAISE EXCEPTION 'Seul le Directeur Executif peut rejeter a cette etape'; END IF;
      ELSE
        RAISE EXCEPTION 'La demande #% est au statut %, elle ne peut pas etre rejetee', p_request_id, v_request.status;
    END CASE;
  ELSE
    IF v_request.status NOT IN ('PENDING'::public.leave_status, 'VALIDATED_RP'::public.leave_status, 'VALIDATED_DC'::public.leave_status) THEN
      RAISE EXCEPTION 'La demande #% est au statut %, elle ne peut pas etre rejetee', p_request_id, v_request.status;
    END IF;
  END IF;

  UPDATE leave_requests SET status = 'REJECTED'::public.leave_status, rejected_by = p_rejector_id,
    rejected_at = NOW(), rejection_reason = TRIM(p_reason), updated_at = NOW() WHERE id = p_request_id;

  RETURN (SELECT to_jsonb(lr.*) FROM leave_requests lr WHERE lr.id = p_request_id);
END;
$$;


-- ── undo_approve_leave_request (FINAL — from 10_combined: FIFO lots restoration) ──

DROP FUNCTION IF EXISTS public.undo_approve_leave_request(BIGINT, UUID) CASCADE;

CREATE OR REPLACE FUNCTION public.undo_approve_leave_request(
  p_request_id BIGINT, p_user_id UUID
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_request      leave_requests%ROWTYPE;
  v_user         utilisateurs%ROWTYPE;
  v_prev_status  TEXT;
  v_field        TEXT;
  v_conge_used   FLOAT;
  v_recup_used   FLOAT;
BEGIN
  SELECT * INTO v_request FROM leave_requests WHERE id = p_request_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'Demande #% introuvable', p_request_id; END IF;

  SELECT * INTO v_user FROM utilisateurs WHERE id = p_user_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'Utilisateur introuvable'; END IF;

  IF v_user.role::TEXT NOT IN ('RH', 'CHEF_SERVICE', 'DIRECTEUR_EXECUTIF', 'ADMIN') THEN
    RAISE EXCEPTION 'Seul un responsable peut annuler une approbation';
  END IF;

  CASE v_request.status
    WHEN 'VALIDATED_RP'::public.leave_status THEN
      v_prev_status := 'PENDING'; v_field := 'rp';
    WHEN 'VALIDATED_DC'::public.leave_status THEN
      v_prev_status := 'VALIDATED_RP'; v_field := 'dc';
    WHEN 'APPROVED'::public.leave_status THEN
      v_prev_status := 'VALIDATED_DC'; v_field := 'de';

      v_conge_used := COALESCE(v_request.balance_conge_used, 0);
      v_recup_used := COALESCE(v_request.balance_recuperation_used, 0);
      IF v_conge_used = 0 AND v_recup_used = 0 THEN
        IF v_request.request_type = 'CONGE' THEN v_conge_used := v_request.days_count;
        ELSE v_recup_used := v_request.days_count; END IF;
      END IF;

      -- CONGE: audit trail only
      IF v_conge_used > 0 THEN
        INSERT INTO leave_balance_history (user_id, type, amount, reason, year, date_from, date_to)
        VALUES (v_request.user_id, 'CONGE', v_conge_used,
          'Annulation approbation demande #' || p_request_id || ' (conge)',
          EXTRACT(YEAR FROM v_request.start_date)::INT, v_request.start_date, v_request.end_date);
      END IF;

      -- RECUPERATION: restore + FIFO lots
      IF v_recup_used > 0 THEN
        UPDATE utilisateurs SET balance_recuperation = balance_recuperation + v_recup_used
        WHERE id = v_request.user_id;

        INSERT INTO leave_balance_history (user_id, type, amount, reason, year, date_from, date_to)
        VALUES (v_request.user_id, 'RECUPERATION', v_recup_used,
          'Annulation approbation demande #' || p_request_id || ' (recuperation)',
          EXTRACT(YEAR FROM v_request.start_date)::INT, v_request.start_date, v_request.end_date);

        DECLARE
          v_lot RECORD; v_remaining FLOAT := v_recup_used; v_room FLOAT; v_credit FLOAT;
        BEGIN
          FOR v_lot IN
            SELECT id, days, remaining_days FROM recovery_balance_lots
            WHERE user_id = v_request.user_id AND expired = false AND remaining_days < days
            ORDER BY expires_at ASC, id ASC
          LOOP
            EXIT WHEN v_remaining <= 0;
            v_room := v_lot.days - v_lot.remaining_days;
            v_credit := LEAST(v_remaining, v_room);
            IF v_credit > 0 THEN
              UPDATE recovery_balance_lots SET remaining_days = remaining_days + v_credit WHERE id = v_lot.id;
              v_remaining := v_remaining - v_credit;
            END IF;
          END LOOP;
        END;
      END IF;
    ELSE
      RAISE EXCEPTION 'La demande #% est au statut %, impossible d''annuler', p_request_id, v_request.status;
  END CASE;

  IF v_prev_status = 'PENDING' AND v_request.initial_status != 'PENDING'::public.leave_status THEN
    v_prev_status := v_request.initial_status::TEXT;
  ELSIF v_prev_status = 'VALIDATED_RP'
        AND v_request.initial_status IN ('VALIDATED_DC'::public.leave_status, 'APPROVED'::public.leave_status) THEN
    v_prev_status := v_request.initial_status::TEXT;
  END IF;

  EXECUTE format(
    'UPDATE leave_requests SET status = $1::public.leave_status, approved_by_%s = NULL, approved_at_%s = NULL, updated_at = NOW() WHERE id = $2',
    v_field, v_field
  ) USING v_prev_status, p_request_id;

  RETURN (SELECT to_jsonb(lr.*) FROM leave_requests lr WHERE lr.id = p_request_id);
END;
$$;


-- ── undo_reject_leave_request ──

DROP FUNCTION IF EXISTS public.undo_reject_leave_request(BIGINT, UUID) CASCADE;

CREATE OR REPLACE FUNCTION public.undo_reject_leave_request(
  p_request_id BIGINT, p_user_id UUID
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_request leave_requests%ROWTYPE; v_user utilisateurs%ROWTYPE; v_restore_status TEXT;
BEGIN
  SELECT * INTO v_request FROM leave_requests WHERE id = p_request_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'Demande #% introuvable', p_request_id; END IF;
  IF v_request.status != 'REJECTED'::public.leave_status THEN
