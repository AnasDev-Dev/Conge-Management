  UPDATE recovery_requests SET status = 'REJECTED', validated_by = p_rejector_id, validated_at = NOW(),
    rejection_reason = p_reason, updated_at = NOW() WHERE id = p_request_id;
  RETURN jsonb_build_object('request_id', p_request_id, 'status', 'REJECTED', 'reason', p_reason);
END;
