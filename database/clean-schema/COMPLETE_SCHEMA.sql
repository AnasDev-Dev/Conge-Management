CREATE OR REPLACE FUNCTION expire_recovery_days()
RETURNS JSONB LANGUAGE plpgsql SECURITY DEFINER AS $$
DECLARE v_lot RECORD; v_count INT := 0; v_total FLOAT := 0;
BEGIN
  FOR v_lot IN SELECT * FROM recovery_balance_lots WHERE expired = false AND expires_at <= CURRENT_DATE AND remaining_days > 0 LOOP
    UPDATE utilisateurs SET balance_recuperation = GREATEST(balance_recuperation - v_lot.remaining_days, 0) WHERE id = v_lot.user_id;
    INSERT INTO leave_balance_history (user_id, type, amount, reason, year)
    VALUES (v_lot.user_id, 'RECUPERATION', -v_lot.remaining_days,
      'Expiration recuperation (acquise en ' || v_lot.year_acquired || ')', EXTRACT(YEAR FROM CURRENT_DATE)::INT);
    UPDATE recovery_balance_lots SET expired = true WHERE id = v_lot.id;
    v_total := v_total + v_lot.remaining_days; v_count := v_count + 1;
  END LOOP;
  RETURN jsonb_build_object('lots_expired', v_count, 'days_expired', v_total, 'date', CURRENT_DATE);
END;
$$;

