-- ── get_active_company_id() ──
-- Reads session variable set by frontend via set_active_company()
CREATE OR REPLACE FUNCTION public.get_active_company_id()
RETURNS BIGINT
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_raw TEXT;
BEGIN
  v_raw := current_setting('app.active_company_id', true);
  IF v_raw IS NULL OR v_raw = '' THEN
    RETURN NULL;
  END IF;
  RETURN v_raw::BIGINT;
EXCEPTION WHEN OTHERS THEN
  RETURN NULL;
END;
$$;

-- ── set_active_company() ──
-- Called by frontend on company switch
CREATE OR REPLACE FUNCTION public.set_active_company(p_company_id BIGINT)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_role TEXT;
  v_is_home BOOLEAN;
  v_dept_id BIGINT;
BEGIN
  SELECT role::TEXT, is_home, department_id
  INTO v_role, v_is_home, v_dept_id
  FROM user_company_roles
  WHERE user_id = auth.uid()
    AND company_id = p_company_id
    AND is_active = true;

  IF NOT FOUND THEN
    SELECT role::TEXT, true, department_id
    INTO v_role, v_is_home, v_dept_id
    FROM utilisateurs
    WHERE id = auth.uid()
      AND company_id = p_company_id;

    IF NOT FOUND THEN
      RAISE EXCEPTION 'Aucun role actif dans cette societe (company_id=%)', p_company_id;
    END IF;
  END IF;

  PERFORM set_config('app.active_company_id', p_company_id::TEXT, false);

  RETURN jsonb_build_object(
    'company_id', p_company_id,
    'role', v_role,
    'is_home', COALESCE(v_is_home, false),
    'department_id', v_dept_id
  );
END;
$$;

-- ── get_my_role() — company-aware with fallback ──
CREATE OR REPLACE FUNCTION public.get_my_role()
RETURNS TEXT
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_company_id BIGINT;
  v_role TEXT;
BEGIN
  v_company_id := get_active_company_id();

  IF v_company_id IS NOT NULL THEN
    SELECT role::TEXT INTO v_role
    FROM user_company_roles
    WHERE user_id = auth.uid()
      AND company_id = v_company_id
      AND is_active = true;

    IF FOUND THEN
      RETURN v_role;
    END IF;
  END IF;

  SELECT role::TEXT INTO v_role
  FROM utilisateurs
  WHERE id = auth.uid();

  RETURN v_role;
END;
$$;

