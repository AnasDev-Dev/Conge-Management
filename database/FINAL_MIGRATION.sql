-- Function to map legacy profile to new role
CREATE OR REPLACE FUNCTION map_legacy_profile_to_role(legacy_profile INT)
RETURNS public.user_role AS $$
BEGIN
    RETURN CASE 
        WHEN legacy_profile IN (1, 16) THEN 'ADMIN'::public.user_role
        WHEN legacy_profile IN (9, 11) THEN 'RESPONSABLE_PERSONNEL'::public.user_role
        WHEN legacy_profile IN (12, 13, 15) THEN 'CHEF_SERVICE'::public.user_role
        ELSE 'EMPLOYEE'::public.user_role
    END;
END;
$$ LANGUAGE plpgsql;

-- Function to map legacy status
CREATE OR REPLACE FUNCTION map_legacy_status(legacy_status TEXT)
RETURNS public.leave_status AS $$
BEGIN
    RETURN CASE 
        WHEN legacy_status = 'Encours' THEN 'PENDING'::public.leave_status
        WHEN legacy_status = 'Valide DC' THEN 'VALIDATED_DC'::public.leave_status
        WHEN legacy_status = 'Valide RP' THEN 'VALIDATED_RP'::public.leave_status
        WHEN legacy_status = 'Valide TG' THEN 'VALIDATED_TG'::public.leave_status
        WHEN legacy_status = 'Valide DE' THEN 'VALIDATED_DE'::public.leave_status
        ELSE 'PENDING'::public.leave_status
    END;
END;
$$ LANGUAGE plpgsql;

-- Function to hash password (simple crypt for now)
CREATE OR REPLACE FUNCTION hash_password(plain_password TEXT)
RETURNS TEXT AS $$
BEGIN
    -- Using crypt extension for password hashing
    RETURN crypt(plain_password, gen_salt('bf', 8));
END;
$$ LANGUAGE plpgsql;

