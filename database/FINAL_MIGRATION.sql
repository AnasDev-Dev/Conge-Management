-- ==============================================================================
-- STEP 2: CREATE CORE TABLES
-- ==============================================================================

-- Companies table
CREATE TABLE IF NOT EXISTS public.companies (
    id bigint generated by default as identity primary key,
    legacy_id text UNIQUE,
    name text NOT NULL UNIQUE,
    created_at timestamptz default now() not null,
    updated_at timestamptz default now() not null
);

-- Departments table
CREATE TABLE IF NOT EXISTS public.departments (
    id bigint generated by default as identity primary key,
    legacy_id bigint UNIQUE,
    name text NOT NULL,
    company_id bigint references public.companies(id) ON DELETE CASCADE,
    filter_budget text,
    filter_article text,
    created_at timestamptz default now() not null,
    updated_at timestamptz default now() not null
);

-- Utilisateurs table (standalone, no auth.users dependency)
CREATE TABLE IF NOT EXISTS public.utilisateurs (
    id uuid default gen_random_uuid() primary key,
    legacy_id bigint UNIQUE,
    email text UNIQUE,
    password_hash text,  -- Will store hashed passwords
    full_name text NOT NULL,
    username text UNIQUE,
    company_id bigint references public.companies(id),
    department_id bigint references public.departments(id),
    job_title text,
    role public.user_role default 'EMPLOYEE',
    is_active boolean default true,
    balance_conge float default 0,
    balance_recuperation float default 0,
    phone text,
    avatar_url text,
    hire_date date,
    birth_date date,
    matricule text,
    cin text,
    cnss text,
    rib text,
    address text,
    city text,
    gender text,
    legacy_profile_id int,
    is_signatory boolean default false,
    signatory_type text,
    signature_file text,
    last_login timestamptz,
    created_at timestamptz default now() not null,
    updated_at timestamptz default now() not null
);

-- ==============================================================================
-- STEP 3: CREATE SUPPORTING TABLES
-- ==============================================================================

-- Leave requests table
CREATE TABLE IF NOT EXISTS public.leave_requests (
    id bigint generated by default as identity primary key,
    legacy_id bigint UNIQUE,
    user_id uuid references public.utilisateurs(id) NOT NULL,
    request_type public.leave_request_type NOT NULL,
    start_date date NOT NULL,
    end_date date NOT NULL,
    days_count float NOT NULL,
    return_date date,
    replacement_user_id uuid references public.utilisateurs(id),
    status public.leave_status default 'PENDING',
    reason text,
    comments text,
    balance_before float,
    balance_conge_used float,
    balance_recuperation_used float,
    approved_by_dc uuid references public.utilisateurs(id),
    approved_by_rp uuid references public.utilisateurs(id),
    approved_by_tg uuid references public.utilisateurs(id),
    approved_by_de uuid references public.utilisateurs(id),
    approved_at_dc timestamptz,
    approved_at_rp timestamptz,
    approved_at_tg timestamptz,
    approved_at_de timestamptz,
    rejected_by uuid references public.utilisateurs(id),
    rejected_at timestamptz,
    rejection_reason text,
    created_at timestamptz default now() not null,
    updated_at timestamptz default now() not null
);

-- Leave balance history
CREATE TABLE IF NOT EXISTS public.leave_balance_history (
    id bigint generated by default as identity primary key,
    user_id uuid references public.utilisateurs(id) NOT NULL,
    type public.leave_request_type NOT NULL,
    amount float NOT NULL,
    reason text,
    year int,
    date_from date,
    date_to date,
    created_at timestamptz default now() not null
);

-- Working days configuration
CREATE TABLE IF NOT EXISTS public.working_days (
    id bigint generated by default as identity primary key,
    company_id bigint references public.companies(id),
    monday boolean default true,
    tuesday boolean default true,
    wednesday boolean default true,
    thursday boolean default true,
    friday boolean default true,
    saturday boolean default false,
    sunday boolean default false
);

-- Holidays
CREATE TABLE IF NOT EXISTS public.holidays (
    id bigint generated by default as identity primary key,
    company_id bigint references public.companies(id),
    name text NOT NULL,
    date date NOT NULL,
    is_recurring boolean default false,
    created_at timestamptz default now() not null
);

-- Notifications
CREATE TABLE IF NOT EXISTS public.notifications (
    id bigint generated by default as identity primary key,
    user_id uuid references public.utilisateurs(id) NOT NULL,
    title text NOT NULL,
    message text NOT NULL,
    type text,
    related_request_id bigint references public.leave_requests(id),
    is_read boolean default false,
    created_at timestamptz default now() not null
);

-- Audit logs
CREATE TABLE IF NOT EXISTS public.audit_logs (
    id bigint generated by default as identity primary key,
    table_name text NOT NULL,
    record_id bigint,
    action text NOT NULL,
    old_data jsonb,
    new_data jsonb,
    user_id uuid references public.utilisateurs(id),
    created_at timestamptz default now() not null
);

