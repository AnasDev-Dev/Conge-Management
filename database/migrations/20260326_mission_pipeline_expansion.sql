-- ── Mission Personnel Categories ──
-- Separate from personnel_categories (which is for leave entitlement).
-- These 4 tiers drive per-diem rates in the tariff grid.

CREATE TABLE IF NOT EXISTS public.mission_personnel_categories (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id BIGINT REFERENCES public.companies(id) ON DELETE CASCADE,
    name TEXT NOT NULL,
    description TEXT,
    sort_order INT NOT NULL DEFAULT 0,
    is_active BOOLEAN NOT NULL DEFAULT true,
    created_at TIMESTAMPTZ DEFAULT now() NOT NULL,
    updated_at TIMESTAMPTZ DEFAULT now() NOT NULL,
    UNIQUE(company_id, name)
);

-- ── Mission Zones ──
-- Geographic zones for international missions (e.g., Europe/Maghreb, USA/UK).

CREATE TABLE IF NOT EXISTS public.mission_zones (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id BIGINT REFERENCES public.companies(id) ON DELETE CASCADE,
    name TEXT NOT NULL,
    description TEXT,
    sort_order INT NOT NULL DEFAULT 0,
    is_active BOOLEAN NOT NULL DEFAULT true,
    created_at TIMESTAMPTZ DEFAULT now() NOT NULL,
    updated_at TIMESTAMPTZ DEFAULT now() NOT NULL,
    UNIQUE(company_id, name)
);

-- ── Mission Tariff Grid ──
-- Rate matrix: one row per (category, zone) pair.
-- Drives per-diem calculation based on employee category + destination zone + PEC.

CREATE TABLE IF NOT EXISTS public.mission_tariff_grid (
    category_id BIGINT NOT NULL REFERENCES public.mission_personnel_categories(id) ON DELETE CASCADE,
    zone_id BIGINT NOT NULL REFERENCES public.mission_zones(id) ON DELETE CASCADE,
    petit_dej NUMERIC(10,2) NOT NULL DEFAULT 0,
    dej NUMERIC(10,2) NOT NULL DEFAULT 0,
    diner NUMERIC(10,2) NOT NULL DEFAULT 0,
    indem_avec_pec NUMERIC(10,2) NOT NULL DEFAULT 0,
    indem_sans_pec NUMERIC(10,2) NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ DEFAULT now() NOT NULL,
    updated_at TIMESTAMPTZ DEFAULT now() NOT NULL,
    PRIMARY KEY (category_id, zone_id)
);


