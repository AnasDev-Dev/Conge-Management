-- ── mission_personnel_categories ──
ALTER TABLE public.mission_personnel_categories ENABLE ROW LEVEL SECURITY;

DO $$ BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE policyname = 'mission_personnel_categories_select' AND tablename = 'mission_personnel_categories'
  ) THEN
    CREATE POLICY "mission_personnel_categories_select"
      ON public.mission_personnel_categories FOR SELECT TO authenticated
      USING (
        company_id IN (
          SELECT ucr.company_id FROM user_company_roles ucr
          WHERE ucr.user_id = auth.uid() AND ucr.is_active = true
        )
        OR company_id = (SELECT company_id FROM utilisateurs WHERE id = auth.uid())
      );
  END IF;
END $$;

DO $$ BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE policyname = 'mission_personnel_categories_manage' AND tablename = 'mission_personnel_categories'
  ) THEN
    CREATE POLICY "mission_personnel_categories_manage"
      ON public.mission_personnel_categories FOR ALL TO authenticated
      USING (public.get_my_role() IN ('ADMIN', 'RH', 'DIRECTEUR_EXECUTIF'))
      WITH CHECK (public.get_my_role() IN ('ADMIN', 'RH', 'DIRECTEUR_EXECUTIF'));
  END IF;
END $$;

