-- ── calculate_annual_entitlement: simplified — employee > department > 18 ──
CREATE OR REPLACE FUNCTION calculate_annual_entitlement(p_user_id UUID)
RETURNS JSONB
LANGUAGE plpgsql
STABLE
AS $$
DECLARE
  v_user              utilisateurs%ROWTYPE;
  v_dept_days         FLOAT;
  v_seniority_date    DATE;
  v_years_of_service  FLOAT;
  v_seniority_periods INTEGER;
  v_base_days         FLOAT := 18.0;
  v_bonus_days        FLOAT := 0;
  v_total_entitlement FLOAT;
  v_max_days          FLOAT := 30.0;
  v_is_minor          BOOLEAN := false;
  v_source            TEXT := 'default';
BEGIN
  SELECT * INTO v_user FROM utilisateurs WHERE id = p_user_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'User % not found', p_user_id; END IF;

  -- Simple priority: employee override > department default > 18
  IF v_user.annual_leave_days IS NOT NULL THEN
    v_base_days := v_user.annual_leave_days;
    v_source := 'employee';
  ELSIF v_user.department_id IS NOT NULL THEN
    SELECT annual_leave_days INTO v_dept_days
    FROM departments WHERE id = v_user.department_id;
    IF FOUND AND v_dept_days IS NOT NULL THEN
      v_base_days := v_dept_days;
      v_source := 'department';
    END IF;
  END IF;

  -- Use date_anciennete for seniority calc, fallback to hire_date
  v_seniority_date := COALESCE(v_user.date_anciennete, v_user.hire_date, CURRENT_DATE);

  IF v_user.birth_date IS NOT NULL THEN
    v_is_minor := (age(CURRENT_DATE, v_user.birth_date) < interval '18 years');
  END IF;
  IF v_is_minor THEN v_base_days := GREATEST(v_base_days, 24.0); END IF;

  v_years_of_service := EXTRACT(EPOCH FROM age(CURRENT_DATE, v_seniority_date)) / (365.25 * 86400);
  v_seniority_periods := FLOOR(v_years_of_service / 5.0)::INTEGER;
  v_bonus_days := v_seniority_periods * 1.5;
  v_total_entitlement := LEAST(v_base_days + v_bonus_days, v_max_days);

  RETURN jsonb_build_object(
    'user_id', p_user_id,
    'seniority_date', v_seniority_date,
    'hire_date', v_user.hire_date,
    'date_anciennete', v_user.date_anciennete,
    'annual_leave_days', v_user.annual_leave_days,
    'years_of_service', ROUND(v_years_of_service::NUMERIC, 1),
    'seniority_periods', v_seniority_periods,
    'base_days', v_base_days,
    'bonus_days', v_bonus_days,
    'annual_entitlement', v_total_entitlement,
    'is_minor', v_is_minor,
    'max_days', v_max_days,
    'department_id', v_user.department_id,
    'entitlement_source', v_source
  );
END;
$$;


