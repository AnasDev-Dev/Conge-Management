-- ── utilisateurs: N+1 hierarchical superior ──
ALTER TABLE public.utilisateurs
  ADD COLUMN IF NOT EXISTS superior_id UUID REFERENCES public.utilisateurs(id);

-- ── utilisateurs: seniority date (separate from hire_date) ──
ALTER TABLE public.utilisateurs
  ADD COLUMN IF NOT EXISTS date_anciennete DATE;

-- ── utilisateurs: per-employee annual leave days ──
-- NULL = inherit from department default. RH can override per employee.
ALTER TABLE public.utilisateurs
  ADD COLUMN IF NOT EXISTS annual_leave_days FLOAT;

-- ── leave_requests: derogation support ──
-- When balance is exhausted, CPA can approve a derogation that borrows from future entitlement
ALTER TABLE public.leave_requests
  ADD COLUMN IF NOT EXISTS is_derogation BOOLEAN DEFAULT false;

ALTER TABLE public.leave_requests
  ADD COLUMN IF NOT EXISTS derogation_approved_by UUID REFERENCES public.utilisateurs(id);

-- ── recovery_requests: link to mission order ──
ALTER TABLE public.recovery_requests
  ADD COLUMN IF NOT EXISTS mission_request_id BIGINT REFERENCES public.mission_requests(id);


