-- ── Exceptional leave types (reference table) ──
CREATE TABLE IF NOT EXISTS public.exceptional_leave_types (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id BIGINT REFERENCES public.companies(id) ON DELETE CASCADE,
    name TEXT NOT NULL,
    description TEXT,
    days_granted FLOAT NOT NULL DEFAULT 3,
    is_active BOOLEAN DEFAULT true,
    created_at TIMESTAMPTZ DEFAULT now() NOT NULL,
    updated_at TIMESTAMPTZ DEFAULT now() NOT NULL,
    UNIQUE(company_id, name)
);

-- ── Exceptional leave claims (per-employee usage) ──
CREATE TABLE IF NOT EXISTS public.exceptional_leave_claims (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID REFERENCES public.utilisateurs(id) NOT NULL,
    exceptional_leave_type_id BIGINT REFERENCES public.exceptional_leave_types(id) NOT NULL,
    leave_request_id BIGINT REFERENCES public.leave_requests(id),
    claim_date DATE NOT NULL DEFAULT CURRENT_DATE,
    days_granted FLOAT NOT NULL,
    notes TEXT,
    approved_by UUID REFERENCES public.utilisateurs(id),
    approved_at TIMESTAMPTZ,
    status TEXT DEFAULT 'PENDING' CHECK (status IN ('PENDING', 'APPROVED', 'REJECTED')),
    created_at TIMESTAMPTZ DEFAULT now() NOT NULL,
    updated_at TIMESTAMPTZ DEFAULT now() NOT NULL
);

-- ── Sick leave tracking (3 days/year authorized by ATH) ──
CREATE TABLE IF NOT EXISTS public.sick_leaves (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID REFERENCES public.utilisateurs(id) NOT NULL,
    start_date DATE NOT NULL,
    end_date DATE NOT NULL,
    days_count FLOAT NOT NULL,
    reason TEXT,
    certificate_url TEXT,
    year INT NOT NULL DEFAULT EXTRACT(YEAR FROM CURRENT_DATE)::INT,
    status TEXT DEFAULT 'PENDING' CHECK (status IN ('PENDING', 'APPROVED', 'REJECTED')),
    approved_by UUID REFERENCES public.utilisateurs(id),
    approved_at TIMESTAMPTZ,
    notes TEXT,
    created_at TIMESTAMPTZ DEFAULT now() NOT NULL,
    updated_at TIMESTAMPTZ DEFAULT now() NOT NULL
);


